<h2>3キャリア対応のケータイ用テンプレートについて</h2>
・Shift_JISでXHTMLを記述してください。<br/>
・全角カタカナは半角カタカナに変換されます。<br/>
・絵文字はDocomoのUnicode形式の記号で記述してください。(自動変換されます。)<br/>
・絵文字の例： [&amp;#xE6FB; ⇒ &#xE6FB;] [&amp;#xE730; ⇒ &#xE730;]<br/>
・フォームにdocomoのistyle属性をつけると自動的に各キャリア用に変換されます。<br/>
